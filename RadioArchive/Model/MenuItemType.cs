namespace RadioArchive
{
    /// <summary>
    /// The type of items for a menu item
    /// </summary>
    public enum MenuItemType
    {
        /// <summary>
        /// show to menu and the text and an icon 
        /// </summary>
        TextAndIcon = 0,
        /// <summary>
        /// Shows a simple divider between the menu items
        /// </summary>
        Divider = 1,
        /// <summary>
        /// Shows a menu text as a header
        /// </summary>
        Header = 2
    }
}
